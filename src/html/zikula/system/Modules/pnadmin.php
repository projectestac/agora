                            'image' => '14_layer_deletelayer.gif',
                            'title' => __('Remove module'));
                        break;
                    case PNMODULE_STATE_MISSING:
                        $actions[] = array(
                            'url' => pnModURL('Modules', 'admin', 'remove', array(
                                'id' => $mod['id'],
                                'startnum' => $startnum,
                                'authid' => $authid,
                                'letter' => $letter,
                                'state' => $state)),
                            'image' => '14_layer_deletelayer.gif',
                            'title' => __('Remove module'));
                        break;
                    case PNMODULE_STATE_UPGRADED:
                        $actions[] = array(
                            'url' => pnModURL('Modules', 'admin', 'upgrade', array(
                                'id' => $mod['id'],
                                'startnum' => $startnum,
                                'authid' => $authid,
                                'letter' => $letter,
                                'state' => $state)),
                            'image' => 'agt_update-product.gif',
                            'title' => __('Upgrade'));
                        break;
                    case PNMODULE_STATE_INVALID:
                        $actions[] = array(
                            'url' => pnModURL('Modules', 'admin', 'remove', array(
                                'id' => $mod['id'],
                                'startnum' => $startnum,
                                'authid' => $authid,
                                'letter' => $letter,
                                'state' => $state)),
