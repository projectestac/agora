	// potential security holes or just too much wasted processing
	if (!SecurityUtil::checkPermission( 'iw_messages::', '::', ACCESS_OVERVIEW) || !pnUserLoggedIn()) {
		return LogUtil::registerPermissionError();
	}

	$uid = pnUserGetVar('uid');

	if ($rpp == '') {
		$sv = pnModFunc('iw_main', 'user', 'genSecurityValue');
		$rpp = pnModFunc('iw_main', 'user', 'userGetVar',
		                  array('uid' => $uid,
								'name' => 'rpp',
								'module' => 'iw_messages',
								'sv' => $sv));
	} else {
		$sv = pnModFunc('iw_main', 'user', 'genSecurityValue');
		pnModFunc('iw_main', 'user', 'userSetVar',
		           array('uid' => $uid,
						 'name' => 'rpp',
						 'module' => 'iw_messages',
						 'sv' => $sv,
						 'value' => $rpp));	
	}

	if ($inici == '') {
		$sv = pnModFunc('iw_main', 'user', 'genSecurityValue');
		$inici = pnModFunc('iw_main', 'user', 'userGetVar',
		                    array('uid' => $uid,
								  'name' => 'inici',
								  'module' => 'iw_messages',
								  'sv' => $sv));
	} else {
		$sv = pnModFunc('iw_main', 'user', 'genSecurityValue');
		pnModFunc('iw_main', 'user', 'userSetVar',
		           array('uid' => $uid,
						 'name' => 'inici',
						 'module' => 'iw_messages',
						 'sv' => $sv,
						 'value' => $inici));
	}

	if ($rppsend == '') {
		$sv = pnModFunc('iw_main', 'user', 'genSecurityValue');
		$rppsend = pnModFunc('iw_main', 'user', 'userGetVar',
		                      array('uid' => $uid,
								    'name' => 'rppsend',
									'module' => 'iw_messages',
									'sv' => $sv));
	} else {
		$sv = pnModFunc('iw_main', 'user', 'genSecurityValue');
		pnModFunc('iw_main', 'user', 'userSetVar',
		           array('uid' => $uid,
						 'name' => 'rppsend',
						 'module' => 'iw_messages',
						 'sv' => $sv,
						 'value' => $rppsend));
	}

	if ($inicisend == '') {
		$sv = pnModFunc('iw_main', 'user', 'genSecurityValue');
		$inicisend = pnModFunc('iw_main', 'user', 'userGetVar',
		                        array('uid' => $uid,
									  'name' => 'inicisend',
									  'module' => 'iw_messages',
									  'sv' => $sv));
	} else {
		$sv = pnModFunc('iw_main', 'user', 'genSecurityValue');
		pnModFunc('iw_main', 'user', 'userSetVar',
		           array('uid' => $uid,
					     'name' => 'inicisend',
						 'module' => 'iw_messages',
						 'sv' => $sv,
						 'value' => $inicisend));
	}

	if ($filtersend == '') {
		$sv = pnModFunc('iw_main', 'user', 'genSecurityValue');
		$filtersend = pnModFunc('iw_main', 'user', 'userGetVar',
		                         array('uid' => $uid,
									   'name' => 'filtersend',
									   'module' => 'iw_messages',
									   'sv' => $sv));
		if ($filtersend == '') {$filtersend = '-1';}
	} else {
		$sv = pnModFunc('iw_main', 'user', 'genSecurityValue');
		pnModFunc('iw_main', 'user', 'userSetVar',
		           array('uid' => $uid,
						 'name' => 'filtersend',
						 'module' => 'iw_messages',
						 'sv' => $sv,
						 'value' => $filtersend));
	}

	if ($filter == '') {
		$sv = pnModFunc('iw_main', 'user', 'genSecurityValue');
		$filter = pnModFunc('iw_main', 'user', 'userGetVar',
		                     array('uid' => $uid,
								   'name' => 'filter',
								   'module' => 'iw_messages',
								   'sv' => $sv));
		if($filter == ''){$filter = '-1';}
	} else {
		$sv = pnModFunc('iw_main', 'user', 'genSecurityValue');
		pnModFunc('iw_main', 'user', 'userSetVar',
		           array('uid' => $uid,
					     'name' => 'filter',
						 'module' => 'iw_messages',
						 'sv' => $sv,
						 'value' => $filter));
	}

	if ($rpp <= 0) {$rpp = 10;}
	if ($inicisend <= 0) {$inicisend = 0;}
	if ($rppsend <= 0) {$rppsend = 10;}
	if ($inici <= 0) {$inici = 0;}

	// The API function is called.  The arguments to the function are passed in
	// as their own arguments array
	$items = pnModAPIFunc('iw_messages', 'user', 'getall',
	                       array('uid' => $uid,
								 'inici' => $inici,
								 'numitems' => $rpp,
								 'filter'=>$filter));
	$items1 = pnModAPIFunc('iw_messages', 'user', 'getallsend',
	                        array('uid' => $uid,
