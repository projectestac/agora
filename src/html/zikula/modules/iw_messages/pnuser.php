													'gid' => $members[1]));
				}
				foreach ($membersList as $member) {
					$usersId[] = $member['id'];
				}	
			}
		}
	}

	if (count($usersId) == 0) {
		LogUtil::registerError (_MESSAGESUSERNOTINDB . ', ' . __('Please check that the name of the user you are sending a message to is a real user and known by this system.', $dom));
	} else {
		if($canUpdate){
