                                        substr($item['msg_time'], 14, 2),     // minute
                                        '0',                                   // second
                                        substr($item['msg_time'], 5, 2),      // month
                                        substr($item['msg_time'], 8, 2),      // day
                                        substr($item['msg_time'], 0, 4));     // year
			
